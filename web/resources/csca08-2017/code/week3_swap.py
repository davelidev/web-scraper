# how to swap two variables
goalkeeper_1 = "John"
goalkeeper_2 = "james"
print ("First goalKeeper="+goalkeeper_1+"\n"+"Second goalKeeper="+goalkeeper_2)

temp = goalkeeper_1
goalkeeper_1 = goalkeeper_2
goalkeeper_2 = temp
print ("First goalKeeper="+goalkeeper_1+"\n"+"Second goalKeeper="+goalkeeper_2)
